        <h2 id="a-3">Как я смотрел фильмы</h2>
        <p>Не всегда получалось смотреть фильм он начала до конца за один присест, но это не мешало мне концентрироваться на фильмах. После каждого просмотра я лез на ютуб и смотрел разборы фильмов и искал там скрытый смысл и то, что я не увидел при просмотре. На протяжении первых 100-150 фильмов я регулярно так делал, после чего начал уже сам обращать внимания на мелочи и детали. </p>


        <h2 id="a-4">Мелочи и детали</h2>
        <p>Со временем я стал обращать внимания на некоторые нюансы фильма. Зачастую именно они стали решающими по установке моего рейтинга. Попробую перечислить их все или почти все:</p>
        <ul>
            <li>Раскрытие всех сюжетных линий. Часто помимо основной сюжетной линии присутствуют несколько других. Например можно вспомнить фильм один дома. Там был дворник, которого так боялся главный герой и у которого была долгая ссора со своей семьей. Кроме основной линии (сможет ли мальчик защитить свой дом) добавляется еще как минимум одна (наладит ли дедушка отношения со своей семьей). И так чем больше второстепенных сюжетных линий, тем фильм интереснее. Особенно интересно в конце сделать вывод все ли они были раскрыты.</li>
            <li>Обложка и описание. Перед просмотром фильма почти всегда читаю описание фильма (краткий пересказ сюжета), а так же стараюсь смотреть на обложку так, как будто я уже посмотрел фильм. Конечно же всегда обращаю внимания на режиссера фильма и вспоминаю, что он снял еще.</li>
            <li>Первый и последний кадр. Зачастую первый кадр очень важен для фильма и задает тон и настроение всего фильма. Интересно знать что именно режиссер поместил в самый первый момент знакомства зрителя со своей картиной.  Последний кадр иногда также о многом говорит. Бывают случаи когда они совпадают.</li>
            <li>Фильм с музыкой или без. Бывают фильмы полностью построенные без музыки. При этом все самые кульминационные эпизоды сняты так, что и без музыки хорошо предаются эмоции.</li>
            <li>Раскрытие преступления. В фильмах, где совершается преступление оно как правило всегда раскрывается. А как вам фильмы в которых этого не происходит? Например как в фильме Видимость, когда полиция так и не раскрывает тайну пропажи молодимого парня.</li>
            <li>Положение камеры. Направлена она сверху вниз на героя или снизу вверх (чтобы показать его никчемность или могущество), сидят герои или стоят (или кто-то один стоит, а другой сидит). Все это связано с психологическим превосходством одного над другими. Это отчетливо видно например в фильме “Молчание ягнят”.</li>
            <li>Освещенность кадра и фильма в целом. Зачастую свет передает настроение, этот фактор думаю не новый и много кому известен. Можно вспомнить фильм “Сталкер” и его серые оттенки на протяжении всего фильма</li>
            <li>Количество склеек в кадре. Практически невозможно встретить экшен- сцену длинной в 20-30 секунд без единой склейки с одной камеры. Тем ценнее, когда такое встречается. Не мало таких продолжительных сцен например в фильме “Схватка”.</li>
            <li>Немного противоположный пункт предыдущему. В некоторых фильмах сюжет построен как рассказы одного события разными людьми. Такие сцены подразумевают снятие одних и тех же сцен под разных ракурсом и с разных камер. Нужно следить за тем на сколько соблюдается точность деталей и положение предметов. Сразу приходит на ум фильм “Точка обстрела” (которого к сожалению нет в данном списке).</li>
            <li>Особенно интересны фильмы, когда пару минут фильма или отдельные его эпизоды  способы перевернуть восприятия фильма, причем как правило в лучшую сторону. Сразу на ум приходит фильм “Свидетель обвинения”.</li>
            <li>Не менее интересно наблюдать за тем с какой скоростью развиваются события в фильме. Так например за 5 минут фильма “Любовное настроение” происходит очень много событий. При этом они хорошо усваиваются и не требуют дополнительной концентрации.</li>
            <li>Иногда посреди фильмов случаются непредсказуемые события. Происходит обманка предсказываемого сюжета. Так например, Альфред Хичкок в фильме “Психо” пригласил известную на то время звезду и красавицу Джанет Ли (возможно именно из-за нее зрители хотели посмотреть этот фильм), а затем режиссер убирает ее посреди фильма. При этом качество фильма и сюжет не страдают.</li>
            <li>Фокусировка. Интересно наблюдать за тем как камера фокусирует элементы в кадре. Так, например в Голливуде любят делать фокусировку только основного объекта и размывать объекты на заднем плане. Такое делал Ридли Скотт уже в 1979 когда снимал фильм “Чужой”. А вот в Болливуде не стесняются лишний раз позвать побольше массовки и детально проработать фон из-за чего кадр становится более реалистичным.</li>
            <li>Обратный отсчет. Часто в фильмах производится обратный отсчет до какого-либо события. Чаще всего до взрыва. В подавляющим большинстве случаем режиссеры небрежно распоряжаются этим временем и умышленно растягивают время.</li>
        </ul>

