        <h2 id="a-8">И еще...</h2>
        <p>Не так давно я пересмотрел все части фильма “Властелин колец”. Парадокс был в том, что я очень давно его не смотрел и практически забыл весь сюжет, будто люди в черном стерли мне память. Тем не менее, я нисколько не пожалел, что пересмотрел этот культовый фильм. Помимо этого пересмотрел 2 части Терминатора и слегка разочаровался фильмом из-за через чур надуманного сюжета, а главное нарушения элементарных законов физики при перемещении во времени, чего скажем не было нарушено в фильме “Назад в будущее”. Точно так же я не так давно посмотрел первый раз фильмы “Чужой” и “Чужие”, который так же хоть и вызвали много нестыковок уже с точки зрения астрономии, но из-за интересного сюжета в целом мне очень зашли. К тому же надо отдать должное данным фильмам ведь первая часть была снята аж в 1979 году. И надо признать снята хорошо. Первый известный советский фильм про инопланетян (пусть и больше был юмористический) был “Кин дза за”, вышедший в тоже время когда и вторая часть “Чужих”, которую снял Кэмерон. Результат сравнения продвинутых технологий у нас и в Голливуде к сожалению кроме схеме ничего друго не вызывает. Тем не менее, при просмотре я всегда давал поблажки старым фильма в виду того, что раньше было гораздо сложнее снимать фильмы, так как не было совершенного компьютерного оборудования для монтажа и сведения, современных камер, дронов в конце концов. Если говорить о фильмах которые я так пока что не посмотрел, то это “Гари Поттер” и “Звездные войны”. Да я тот человек, который не смотрел ни одной части ни того, ни другого. С большой долей вероятности когда-нибудь я их посмотрю, но это уже совсем другая история.</p>

        <h2 id="a-9">Кинофестивали и церемонии</h2>
        <p>В какой-то момент мне стало интересно какие бывают кинофестивали и церемонии. Кроме церемонии Оскар и Каннского, Венецианского и Берлинского (нет, не пациента) кинофестивалей я про другие мало что слышал. Поискав в интернете я понял, что их гораздо больше. Однако углубляться и становиться пристальным зрителем подобных мероприятий у меня планов нет.</p>

        <h2 id="a-10">Выводы</h2>
        <p>Я нисколько не пожелал что проделал весь этот путь и в течении больше чем года смотрел эти фильмы, а так же потом обзоры и поиски скрытых смыслов на некоторые из фильмов. Не претендую на роль кино-эксперта, но могу сказать что в фильмах я стал разбираться чуть лучше чем пару лет назад. Сейчас я наверно не пойду в кинотеатр, пока не узнаю режиссера фильма и не почитаю его описание. Конечно же по возможности посмотрю и актерский состав. Как я писал вышел в разделе обучения - фильмы являются хорошим способом изучения биографии отдельных людей, а так же изучения истории в целом. На этом мое увлечение просмотров фильмами не заканчивается. Возможно я стану смотреть их чуть меньше, но впереди еще есть несколько списков которые я хотел бы обработать. Такие как “Кинопоиск” - хотя бы фильмов 100. А так же топ 100 фильмов разных жанров. В будущем так же думаю вернусь и пересмотрю некоторые фильмы из рейтинга IMDb. Посмотрю другие фильмы моих любимых режиссеров не вошедшие в этот список. Возможно так же я обращу внимание на мюзиклы, ведь пока смотрел фильмы IMDb, там я столкнулся с мюзиклом “Гамильтон”. И надо признать понравился, хотя это и было для меня что-то новое и странное. Иными словами мне еще есть что посмотреть и чем заниматься в области классики мирового кино.</p>
