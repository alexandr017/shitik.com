        <h2 id="a-1">Введение</h2>
        <p>Продолжение статьи про выбор и <a href="/blog/home-bay" target="_blank">покупку квартиры</a> настало время написать детали по ремонту квартиры. Статья выходит более чем, через год после выхода прошлой, однако точные сроки ремонта тяжело определить и ремонт однозначно не занял столько времени, потому чтобы были периоды, когда я ставил ремонт на паузу из-за определённых событий. Впрочем, обо всем этом далее.</p>


        <h2 id="a-2">Ход работ</h2>
        <p>Сразу после покупки квартиры осенью я занялся демонтажами работами и подготовкой квартиры к будущему ремонту. Также осенью поменял все стеклопакеты на более новые. Ближе к зиме удалось в короткие сроки полностью обновить ванну и туалет. Это при том что менялись не только вся сантехника, но и полностью все трубы, кроме стояка канализации.</p>
        <p>Далее была зимняя пауза, вызванная как минимум 3мя препятствиями:<br>
            1. Захотелось несколько месяцев подкопить денег, чтобы купить авто из салона.<br>
            2. Захотелось поменять радиаторы отопления и в отопительный сезон это сложнее сделать.<br>
            3. Захотелось обновить стяжку и ее доставка в мороз, также крайне не желательна.<br>
        </p>
        <p>Примерно в феврале решили обратиться к дизайнеру чтобы можно было спроектировать интерьер оставшейся части квартиры. Через 3-4 недели получили окончательно утвержденный нами план по которому можно было приступать к ремонту. После того как в марте я купил вторую машину и подкопил денег на то, чтобы можно было начать ремонт в мае начались работы по ремонту лоджии. Сроки постоянно смещались из-за того, что бригада не успевала прошлые объекты (в прочем ничего нового), но в последней недели мая активно начались работы по лоджии. Лоджию утеплили, провели электричество. Сделали шкаф и ларь для хранения. Обшили стены и потолок, на пол положили ламинат. Сделали подвесную систему для сушки белья. Установили премиум подоконник и самые плотные из предложенных стеклопакетов, в которых все створки окон сделали открывающими. Заказали несколько москитных сеток. Суммарно лоджия обошлась более чем в 3000$. Более детальные суммы можно посмотреть в таблице ниже.</p>
        <p>Параллельно с балконом уже другая бригада начала работы по демонтажу старой стяжки, а также прочим демонтажами работам, которые я не закончил осенью, а именно снятие части обоев, потолка в одной и комнату, старой плитки на фартуке, демонтаж старых розеток и некоторые другие работы. Забегая наперед скажу, что эта бригада и выполняла основную часть ремонта квартиры. Все же полноценно приступить к ремонту они не могли из-за бригады балкончиков. Тем не менее за это время они уже успели снять старую стяжку, закупить и привезти материалы для будущей заливки. При этом стяжка содержала несколько слоем и конечный слой, ровно, как и демонтаж направляющих осуществляются только через месяц после залитая первого слоя стяжки.</p>
        <p>Параллельно со стяжкой мы заметили входную дверь, а также все радиаторы отопления. В это же время начались масштабные работы по прокладке новой электрики по всей квартире. Прокладка осуществлялась по потолку, так как это наиболее просто вариант. Данные провода можно без проблем скрыть натяжными потолками. Проводов было очень много. На кухне практически вся техника имеет свои электрические провода. Кроме того помимо классической проводки были кабеля для ТВ, интернета и много проводов для подсветки светодиодных лент. По этой же причине в прихожей было установлено два ящика с трансформаторами. Один для трансформаторов светодиодных лент, другой для автоматов. Розетки и выключатели были установлены значительно позже через 3-4 месяца.</p>
        <p>После того как была готова стяжка рабочие приступили к выравниванию стен. В некоторых местах снималась старая штукатурка, в некоторых просто докладывалась новая. Выравнивание стен необходимо было по нескольким важным причинам:<br>
            1. Мы планировали встроенную мебель<br>
            2. Мы планировали плинтуса с подсветкой. А это определённый тип материала, который не гнется и соответственно все кривые стены не позволили прикрепить такой плинтус.<br>
        </p>

        <p>Выравнивание стен с последующей подготовкой их к декоративной штукатурке в одних комнатах и обоям в других заняло около месяца. Это было связано в первую очень с тем, что требовалось наносить огромные слои штукатурки. Прежде чем приступать к следующему шагу штукатурка должна просохнуть. Поэтому все работы заняли действительно много времени. Наши стены утяжелились на полторы тоны только сухих смесей. В сочетании с водой этот показатель значительно возрос, так что реальный вес стен значительно возрос.</p>
        <p>Наконец, все черновые работы по стенам закончились и можно было двигаться дальше. Заниматься непосредственно отдельной. Этот процесс на удивление прошел быстро и за несколько недель были поклеены обои в зале и спальне. Нанесена декларативная штукатурка в прихожей и кухне. Был уложен ламинат в зале и спальне, а также уложена плитка в кухне и прихожей. Квартира уже выглядела достаточно красиво хоть в ней еще не было мебели, потолков, плинтусов, межкомнатных дверей и розеток с выключателями.</p>
        <p>Первым делом из этого списка были установлены межкомнатные двери. Достаточно быстро были установлены розетки и выключатели. В квартире по-прежнему не было мебели и натяжных потолков, но это уже мне не помешала переехать. При этом на момент переезда уже в течение нескольких недель должна была подъехать некоторая мебель. Также я устал регулярно в течении рабочего времени и выходных отлучаться на квартиру по разным вопросам и хотелось уже оставшуюся часть ремонта решать непосредственно находясь в квартире. Чтобы хоть как-то жить в квартире мы достаточно быстро заказали жалюзи.</p>
        <p>Так как ремонт еще не был полноценно завершенным много мебели заказывать не хотелось. Она бы занимала много места и встроенную мебель, которая приезжала разобранной вместе с инструментами рабочих также занимала бы много места и коробки и инструменты некуда было бы ставить. К тому же сильно раньше заказать мебель и не получилось. Эффект санкций внес хоть и небольшие, но трудности. Как правило, все заказы немного сдвинулись. Мы сильно заранее заказали кухонную технику, так как она была вся встроенной и необходимы были точные ее размеры для встраивания в кухню. Вообще стоит отметить, что практически вся мебель в квартиру была куплена. Исключением являются мой телевизор, который я покупал, когда еще жил на съёмной квартире, а также стиральная машина супруги, которую она покупала также живя на съёмной квартире. Первыми элементами не встроенной мебели были компьютерные стол с регулируемой высотой и компьютерное кресло, диван, кухонные стулья и стол. Кровать приехала лишь через месяца два, но и выбрали и заказали мы ее не сразу. Со встроенной мебелью, которой было достаточно много ждать приходилось еще дольше, но это того стоило, абсолютно вся встроенная мебель, которую мы заказывали по сути просто по картинкам, проектируя сами мебель на листике или через сайты в итоге по изготовлению нам очень нравилась. Одним из первых элементов встроенной мебели была готова ТВ-зона. Далее была наконец-то готова кухня. На кухне при этом стоит детальней остановится, так как достаточно долго выбирали раковину, смеситель и столешницу. Последняя к слову из камня и обошлась почти в 1000$. Также из встроенной мебели было 2 шкафа: в спальне и прихожей. Шкаф, который расположен в прихожей скрыл оба электрощита. Проектирование обоих шкафов мы занимались сами через какой-то сайт конструктор. Далее ребята без проблем воплотили его в реальный объект. Кроме этого у них мы заказали и еще один небольшой шкаф в зал, но уже не встроенный.</p>
        <p>И так, по завершению встроенной мебели наконец-то можно было приступить к натяжным потолкам. Необходимо было соблюдать именно такую последовательность, так как встроенная мебель была высотой до потолка и посуди потолок в местах соприкосновений с такой мебелью крепился именно к мебели. Натяжные потолки сделали достаточно быстро, но тем не менее на данном этапе было чувство разочарования. Во-первых, то, что натяжные потолки дешёвые это оказалось заблуждением. Конечно, если сделать классические потолки без углов, без светильников и без встроенных карнизов, то ты платишь за материал и его быструю установку, так как нет никаких преград и деталей. В нашем же случае было все совсем не так, так было очень много светильников, скрытые карнизы и углы. Во-вторых, если цена не так сильно разочаровала, то сама процесс установки был очень пыльным. Мы укрыли часть мебели, но не были готовы, что будет так много мусора и пыли. Пришлось после этого делать капитальную уборку квартиру. Очередную большую уборку, которые мы делали и при заселении и при установке некоторой мебели. Возвращаясь к светильникам и карнизам у нас было 2 скрытых карниза в зале и спальне. Три люстры (на кухне, спальне и зале), 13 одиночных спотов (одиночных светильников) суммарно по всей квартире, а также еще 4 трековых светильника .</p>
        <p>Примерно вместе с потолками мы наконец-то установили себе плинтуса. Сложности в установки особой не было, да и сами плинтуса мы закупили сильно раньше. Ранняя покупка была обусловлена тем, что нужно было точно знать размеры плинтусов для встроенной мебели. Плинтуса устанавливались после встроенной мебели, чтобы не устанавливать их там, где они не нудны, например, за кухней и встроенными шкафами. Сложность установки еще была в том, что в коридоре и кухне плинтуса были с подсветкой, по этому помимо того, что их нужно было просто установить, нужно было также установить светодиодные ленты и трансформатор.</p>
        <p>Выше я говорил, что было много проводки для подсветки, при этом нигде, кроме предложения выше не описал, где именно используется светодиодная подсветка в квартире. Перечислю все места, где есть светодиодные ленты:</p>
        <ul>
            <li>Плинтуса кухни и коридора</li>
            <li>Рабочая поверхность кухни</li>
            <li>Под подоконником на лоджии</li>
            <li>ТВ-зон в зале</li>
            <li>Шкаф и полка над диванов в зале</li>
            <li>В коридоре возле зеркала</li>
            <li>В спальне возле туалетного столика</li>
            <li>Зеркало в туалете</li>
        </ul>
        <p>На этом ремонт практически был завершен. Все это было готово уже к концу 2022 года. Из того что осталось на 2023 год было: подвесная полка над кроватью, туалетный столик и зеркало с подсветкой в спальне, тумбочка с зеркалом с подсветкой в коридоре. Это были небольшие элементы без которых мы достаточно хорошо справлялись первые месяцы проживания. Тем не менее определившись с типами зеркал и цветом дерева в марте мы заказали данные элементы.</p>
        <p>Еще один элемент интерьера, который мы не спешили заказывать были шторы и тюль. У нас были жалюзи, которые мы установили при переезде, так что у нас было достаточно времени на, чтобы выбрать те шторы, которые бы пришлись по вкусу. Так что шторы у нас появились только в самом конце марта.</p>
        <p>На этом ремонт можно окончательно считать завершенным. В общей сложности мы потратили почти 50 000$ . Это огромная сумма, котора практически ровняется стоимости квартиры. При этом стоит повторно отметить, что при переезде мы не покупали телевизор и стиральную машину. Тем не менее есть несколько элементов, которые все же хочется докупить в ближайшее время. Хоть есть многофункциональный пуфик на 6 стульев хочется приобрести кресло для туалетного столика. Также текущий телевизор лучше повесить в спальню или на кухню и в зал на ТВ зону поставить телек в 2 раза больше. Это как минимум закроет розетки. Вообще надо понимать, что технически есть возможность использовать 3 телевизора в квартире: кухня, спальня, зал, но пока что есть только, поэтому возможные покупки еще 2ух телевизор и 2ух кронштейнов крепления. Помимо телевизоров также хочется купить 2 монитора для крепления на компьютерный стол. Если подвести итог недостающих элементов: кресло, 2 телевизора, 2 монитора, то общая стоимость затрат на квартиру возрастет еще на несколько тысяч долларов.</p>
