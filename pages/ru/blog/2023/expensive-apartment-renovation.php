        <h2 id="a-3">Качество проделанной работы и косяки</h2>
        <p>Обычно про плохие нюансы ремонта не принято говорить, особенно если ремонт в районе 50 000$, но все же попробуем разобрать бригады, которые делали нам ремонт, а также оценить качество их работ.</p>
        <p>Ванной и туалетом занималась бригада “А”. Сделали под ключ буквально за пару недель. Все очень понравилось, если бы не одно большое но. Во время соедини трубы из ванной к основному стояку протекала прокладка и из-за этого мы подтопили соседей снизу. Ремонт соседей снизу обошелся достаточно дешево, но помимо ремонта снизу нам пришлось снова в туалете ломать плитку на задней стенке, периодически снимать инсталляцию унитаза и долгое время (до обратной установки новой плитки) с дискомфортом ходить в туалет и сидеть на унитазе только в пол веса. Суммарно ремонт туалета и соседней снизу, который хоть и был по вине бригады обошелся нам примерно в 400-600 долларов.</p>
        <p>Бригада “В” под ключ делала лоджию. Результаты в целом были более, чем удовлетворительные. К сожалению они долго приступали к работам. Также были очень мелкие косяки, например, забрызгали при покраске точечно стекло краской, повредили москитную сетку и т.п. Практически все свои косяки оперативно исправили.</p>
        <p>Бригада “С” делала основную часть ремонта, от стяжки до отделочных работ. Надо признать, что сделали достаточно хорошо, отдельное спасибо прорабу. Хотя цена на некоторые виды работ, например замена радиаторов отопления, мне показалось завышена.</p>
        <p>Бригада “D” делала всю встроенную мебель и также сделали все очень хорошо. На некоторые элементы цена казалась явно высокой, например межкомнатные стеклянные двери обошлись почти в 1000$. Правда такие двери и правда сильно дешевле нигде не заказать. Но с другой стороны у них были достаточно дешевые зеркала и некоторая встроенная мебель. Так что в целом мы остались бригадой довольны.</p>
        <p>Кроме выше перечисленных бригад были бригады по первоначальному вывозу мусора, по замене окон, по натяжке потолков и установке плинтусов. Работы всех, кроме натяжных потолков были удовлетворительными.</p>

